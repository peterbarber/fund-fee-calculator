
public class Fund {

	private String name;
	private String memberFee;
	private String adminFee;
	private String investmentFee;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMemberFee() {
		return memberFee;
	}
	public void setMemberFee(String memberFee) {
		this.memberFee = memberFee;
	}
	public String getAdminFee() {
		return adminFee;
	}
	public void setAdminFee(String adminFee) {
		this.adminFee = adminFee;
	}
	public String getInvestmentFee() {
		return investmentFee;
	}
	public void setInvestmentFee(String investmentFee) {
		this.investmentFee = investmentFee;
	}
	
	

}
